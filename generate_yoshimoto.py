def katakana_to_phonemes(katakana: str) -> str:
    """カタカナ文字列を音素列（スペース区切り）に変換する."""
    result = []
    i = 0
    while i < len(katakana):
        # 促音: 次の文字の子音を重ねる
        if katakana[i] == "ッ":
            # 次の文字の最初の子音を探す
            if i + 1 < len(katakana):
                next_phonemes = _get_next_phonemes(katakana, i + 1)
                if next_phonemes and next_phonemes[0] not in ("a", "i", "u", "e", "o"):
                    result.append(next_phonemes[0])
                else:
                    result.append("q")  # fallback
            i += 1
            continue

        # 長音: 前の母音を繰り返す
        if katakana[i] == "ー":
            if result:
                # 最後の母音を見つけて繰り返す
                last_vowel = None
                for p in reversed(result):
                    if p in ("a", "i", "u", "e", "o"):
                        last_vowel = p
                        break
                if last_vowel:
                    result.append(last_vowel)
            i += 1
            continue

        matched = False
        # 長いパターンから優先マッチ
        for pattern, phonemes in KATAKANA_TO_PHONEME:
            plen = len(pattern)
            if katakana[i:i + plen] == pattern:
                result.extend(phonemes)
                i += plen
                matched = True
                break

        if not matched:
            # 未知文字はスキップ
            i += 1

    return " ".join(result)


def _get_next_phonemes(katakana: str, pos: int) -> list:
    """指定位置から始まる音素列を返す（促音処理用）."""
    for pattern, phonemes in KATAKANA_TO_PHONEME:
        plen = len(pattern)
        if katakana[pos:pos + plen] == pattern:
            return phonemes
    return []


