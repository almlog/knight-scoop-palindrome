def normalize_name(name: str) -> str:
    """芸名を正規化する（記号除去など）."""
    # 記号や特殊文字を除去
    name = name.replace("!", "").replace("!!", "").replace("♪", "")
    name = name.replace("☆", "").replace("。", "").replace("～", "ー")
    name = name.replace("・", "").replace("  ", " ").strip()
    return name


