def main():
    """メイン処理: 読み推定→音素変換→回文判定→CSV出力."""
    output_path = os.path.join(
        os.path.dirname(os.path.abspath(__file__)),
        "yoshimoto_palindrome.csv"
    )

    # 重複除去（名前ベース）
    seen = set()
    unique_data = []
    for name, category in RAW_DATA:
        clean = normalize_name(name)
        if clean not in seen:
            seen.add(clean)
            unique_data.append((name, category))

    rows = []
    palindrome_count = 0

    for name, category in unique_data:
        reading = to_katakana_reading(name)
        if not reading:
            continue

        phonemes = katakana_to_phonemes(reading)
        if not phonemes:
            continue

        is_palindrome = is_strict_palindrome(phonemes)
        judgment = "〇" if is_palindrome else "×"
        if is_palindrome:
            palindrome_count += 1

        rows.append({
            "品詞": "芸人",
            "品詞細分類": category,
            "ワード": normalize_name(name),
            "ヨミガナ": reading,
            "音素": phonemes,
            "回文判定": judgment,
            "学名": "-",
        })

    with open(output_path, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=["品詞", "品詞細分類", "ワード", "ヨミガナ", "音素", "回文判定", "学名"]
        )
        writer.writeheader()
        writer.writerows(rows)

    print(f"CSV出力完了: {output_path}")
    print(f"総エントリ数: {len(rows)}")
    print(f"回文判定〇: {palindrome_count}")
    print(f"回文判定×: {len(rows) - palindrome_count}")

    # 回文になったものを表示
    if palindrome_count > 0:
        print("\n=== 回文判定〇 の芸人 ===")
        for r in rows:
            if r["回文判定"] == "〇":
                print(f"  {r['ワード']} ({r['ヨミガナ']}) → {r['音素']}")


if __name__ == "__main__":
    main()
